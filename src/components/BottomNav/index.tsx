const TABS: { key: NavTab; label: string; icon: React.ReactNode }[] = [
  { key: 'home', label: '홈', icon: <HomeIcon /> },
  { key: 'register', label: '등록', icon: <RegisterIcon /> },
  { key: 'my', label: 'MY', icon: <MyIcon /> },
]

const NAV_PATHS: Record<Exclude<NavTab, 'home'>, string> = {
  register: '/entry',
  my: '/my',
}

function BottomNav({ active = 'home', onTabChange }: BottomNavProps) {
  const navigate = useNavigate()
  const location = useLocation()
  const { user } = useAuth()

  const visibleTabs = user?.role === 'admin'
  ? TABS.filter((tab) => tab.key !== 'register')
  : TABS

  function getHomePath() {
    if (user?.role === 'admin' || location.pathname.startsWith('/admin')) {
      return '/admin'
    }

    return '/main'
  }

  function handleTabClick(tab: NavTab) {
    if (onTabChange) {
      onTabChange(tab)
      return
    }

    const path = tab === 'home' ? getHomePath() : NAV_PATHS[tab]

    if (location.pathname !== path) {
      navigate(path)
    }
  }

  return (
    <nav className="bottom-nav">
      {visibleTabs.map(({ key, label, icon }) => (
        <button
          key={key}
          type="button"
          className={`bottom-nav-item ${active === key ? 'bottom-nav-item-active' : ''}`}
          onClick={() => handleTabClick(key)}
        >
          {icon}
          <span>{label}</span>
        </button>
      ))}
    </nav>
  )
}

export default BottomNav
